        <p>Dr. Therabot can be used for a variety of purposes, including:</p>
        <ul>
            <li>Counseling: Dr. Therabot can provide users with counseling and support on a variety of mental health issues, including anxiety, depression, stress, and trauma.</li>
            <li>Education: Dr. Therabot can provide users with information about mental health and mental health resources.</li>
            <li>Peer support: Dr. Therabot can connect users with other people who are struggling with similar mental health issues.</li>
        </ul>

        <p>Dr. Therabot is completely anonymous. Chats are not saved other than whatever OpenAI saves on their end. This means that users can be assured that their privacy will be respected.</p>


